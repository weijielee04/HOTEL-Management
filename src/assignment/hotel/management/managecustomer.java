    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = jTable1.getSelectedRow();
        TableModel model=jTable1.getModel();
        usernameadd.setText(model.getValueAt(i,0).toString());
        passwordadd.setText(model.getValueAt(i,1).toString());
        String gender = model.getValueAt(i,2).toString();
        if(gender.equals("Male")){
            Male.setSelected(true);
        }
        else{
            Female.setSelected(true);
        }
        String Role = model.getValueAt(i,3).toString();
        if(Role.equals("Manager")){
            Manager.setSelected(true);
        }
        else{
            Technician.setSelected(true);
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

